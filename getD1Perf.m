function s = getD1Perf(t)
f1=@(x)(0.9896 - 0.3350745629030397*exp(-1/2.14*x)).*(x>=0&x<8)...
    +(1.0674452453565133*exp(-2*16/32/32*x) + 0.153).*(x>=8&x<24)...
    +(0.9896 - 0.3350745629030397*exp(-1/2.14*(x-24))).*(x>=24&x<32)...
    +(1.0674452453565133*exp(-2*16/32/32*(x-24)) + 0.153).*(x>=32&x<=48);

f2=@(x)(0.9896 - 0.3350745629030397*exp(-1/2.14*(x-t))).*(x>=t&x<(8+t))...
    +(1.0674452453565133*exp(-2*16/32/32*(x-t)) + 0.153).*(x>=(8+t)&x<(24+t))...
    +(0.9896 - 0.3350745629030397*exp(-1/2.14*(x-24-t))).*(x>=(24+t)&x<(32+t))...
    +(1.0674452453565133*exp(-2*16/32/32*(x-24-t)) + 0.153).*(x>=(32+t)&x<=(48+t));

f3=@(x)f1(x)-f2(x);

% x=0:0.001:48;
% y1=f1(x);
% y2=f2(x);
% y3=f3(x);
% 
% plot(x,y1);
% hold on;
% plot(x,y2);
% plot(x,y3);

xi=24;
dx=5;
xf=48;
for k=xi:dx:xf
    i=round((k-xi)/dx)+1;
    icpt(i)=fsolve(f3,k);
end

[A,B]=unique(roundn(icpt,-1));
C=icpt(B);
findr=find(A>24&A<48);
icpt=C(findr)

s=quad(f1,xi,icpt(1))+quad(f2,icpt(1),icpt(2))+quad(f1,icpt(2),xf);